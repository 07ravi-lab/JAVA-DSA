package BasicsOfJava;

public class DoubleData {
    public static void main(String[] args) {
        double x=4;
        double y = 5;
        double z =6;
        double sum = x+y+z;
        System.out.println(sum);
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
    }
}
